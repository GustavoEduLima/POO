package ExemploHeranca;

public class Equipamento{
    private String dtAquisicao;
    private double preco;
    private double conservacao;

    public String getDtAquisicao() {
        return dtAquisicao;
    }
    public void setDtAquisicao(String dtAquisicao) {
        this.dtAquisicao = dtAquisicao;
    }
    public double getPreco() {
        return preco;
    }
    public void setPreco(double preco) {
        this.preco = preco;
    }
    public double getConsercavacao() {
        return conservacao;
    }
    public void setConsercavacao(double conservacao) {
        this.conservacao = conservacao;
    }

    
}